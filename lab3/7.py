class Point:
    
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def show(self):
        print(f"({self.x}, {self.y})")

    def move(self, new_x, new_y):
        self.x = new_x
        self.y = new_y

    def dist(self, other_point):
        return ((self.x - other_point.x) ** 2 + 
                (self.y - other_point.y) ** 2) ** 0.5


# -------- INPUT --------
x1, y1 = map(int, input().split())
new_x, new_y = map(int, input().split())
x2, y2 = map(int, input().split())

# бірінші нүкте
p1 = Point(x1, y1)

# бастапқы координата
p1.show()

# move жасаймыз
p1.move(new_x, new_y)

# жаңа координата
p1.show()

# екінші нүкте
p2 = Point(x2, y2)

# қашықтық
distance = p1.dist(p2)

# 2 таңбаға дейін форматтау
print(f"{distance:.2f}")
